//falsy values: undefined, null, NaN (Not a Number), 0, '', false

//Equal
"3" == 3;

//Strict Equal
"3" === 3;      // Have to be the same number & data type.

//Not Equal
"3" != 4;

//Strict Not Equal
"3" !== 3;

//Greater Than
3 > 2;      //True

//Less Than
3 < 2;      //False

//Greater Than or Equal To
3 >= 2;     //True

//Less Than or Equal To
3 <= 2;

//And
2 > 1 && 2 < 5;     //True

//Or
2 > 1 || 2 < 5;     //True
